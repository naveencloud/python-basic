# Sort Will work only with the Homogeneous Value

items = [2.2, 1, 100, 99, 50 , 150, 5.5, 10, 7.5, 0.00045, 0.00050]
items.sort() # it will sort the lsit
print(items)
print()

# Revesring the list and writing to the content
items.reverse()
print(items)
print()

# Reverse is the "keyword" to do Desending Order
items.sort(reverse=True)
print(items)
print()